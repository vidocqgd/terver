# Задача 2. В результате 10 независимых измерений некоторой величины X, выполненных с одинаковой точностью,
# получены опытные данные: 6.9, 6.1, 6.2, 6.8, 7.5, 6.3, 6.4, 6.9, 6.7, 6.1
# Предполагая, что результаты измерений подчинены нормальному закону распределения вероятностей,
# оценить истинное значение величины X при помощи доверительного интервала, покрывающего это значение с доверительной вероятностью 0,95.




import numpy as np
from scipy.stats import t

# Опытные данные
data = [6.9, 6.1, 6.2, 6.8, 7.5, 6.3, 6.4, 6.9, 6.7, 6.1]

# Рассчитываем среднее значение и стандартное отклонение выборки
mean = np.mean(data)
std = np.std(data, ddof=1)

# Рассчитываем доверительный интервал с доверительной вероятностью 0.95
n = len(data)
alpha = 1 - 0.95
t_critical = t.ppf(1 - alpha / 2, df=n - 1)
margin_of_error = t_critical * std / np.sqrt(n)
confidence_interval = (mean - margin_of_error, mean + margin_of_error)

# Выводим результаты
print("Среднее значение: {:.2f}".format(mean))
print("Доверительный интервал: ({:.2f}, {:.2f})".format(confidence_interval[0], confidence_interval[1]))