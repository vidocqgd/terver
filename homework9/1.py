# Задача 1 Даны значения величины заработной платы заемщиков банка (zp) и значения их
# поведенческого кредитного скоринга (ks): zp = [35, 45, 190, 200, 40, 70, 54, 150, 120, 110],
# ks = [401, 574, 874, 919, 459, 739, 653, 902, 746, 832]. Используя математические
# операции, посчитать коэффициенты линейной регрессии, приняв за X заработную плату
# (то есть, zp - признак), а за y - значения скорингового балла (то есть, ks - целевая
# переменная). Произвести расчет как с использованием intercept, так и без.

import numpy as np

# Заданные значения заработной платы и скорингового балла
zp = np.array([35, 45, 190, 200, 40, 70, 54, 150, 120, 110])
ks = np.array([401, 574, 874, 919, 459, 739, 653, 902, 746, 832])

# Расчет коэффициентов линейной регрессии с использованием intercept
X = np.column_stack((np.ones(len(zp)), zp))
coefficients_with_intercept = np.linalg.inv(X.T @ X) @ X.T @ ks

# Расчет коэффициентов линейной регрессии без использования intercept
coefficients_without_intercept = np.linalg.inv(zp.reshape(-1, 1).T @ zp.reshape(-1, 1)) @ zp.reshape(-1, 1).T @ ks.reshape(-1, 1)

print("Коэффициенты линейной регрессии с использованием intercept:", coefficients_with_intercept.flatten())
print("Коэффициенты линейной регрессии без использования intercept:", coefficients_without_intercept.flatten())