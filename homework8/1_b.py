# Задача 1 Даны значения величины заработной платы заемщиков банка (zp) и значения их
# поведенческого кредитного скоринга (ks):
# zp = [35, 45, 190, 200, 40, 70, 54, 150, 120, 110],
# ks = [401, 574, 874, 919, 459, 739, 653, 902, 746, 832].
# Найдите ковариацию этих двух величин с помощью элементарных действий, а затем с
# помощью функции cov из numpy
# Полученные значения должны быть равны.
# Найдите коэффициент корреляции Пирсона с помощью ковариации и
# среднеквадратичных отклонений двух признаков,
# а затем с использованием функций из библиотек numpy и pandas.


import numpy as np
import pandas as pd

zp = [35, 45, 190, 200, 40, 70, 54, 150, 120, 110]
ks = [401, 574, 874, 919, 459, 739, 653, 902, 746, 832]

# Вычисление ковариации с помощью NumPy
cov_matrix = np.cov(zp, ks, ddof=1)
covariance = cov_matrix[0, 1]

print("Ковариация (с помощью NumPy):", covariance)

# Вычисление коэффициента корреляции Пирсона с помощью NumPy
corr_coef = np.corrcoef(zp, ks)[0, 1]

print("Коэффициент корреляции Пирсона (с помощью NumPy):", corr_coef)

# Вычисление коэффициента корреляции Пирсона с помощью Pandas
df = pd.DataFrame({'zp': zp, 'ks': ks})
corr_coef_pd = df['zp'].corr(df['ks'])

print("Коэффициент корреляции Пирсона (с помощью Pandas):", corr_coef_pd)